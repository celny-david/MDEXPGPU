/* Function: lj_elfor
 * device function which returns the potential energy value for given distance
 *
 * perform lookup in the spline and calculates value of the polynom
 *
 * Notes:
 * > this is the version for the electrostatic interaction
 * > performs out of the bounds check
 * > uses the #defined variables for spline parameters
 * > implemented for 2 - 4 column flattened potentials
 *
 * Parameter:
 * x - the distance value
 */
__device__ value_t lj_elfor(value_t x)
{
  unsigned int i;

  i=(unsigned int)(x*ELPOT_WIDTH);
  if (i<ELMIN_SIZE || i> ELMAX_SIZE)
  {
    // printf("elspline: arg (%g) out of range \n",x); // DEBUG
  	return 0.0;
  }
#if ELFOR_COLL == 2
	  i *= 2;
	  return d_for_elspline[i]+x*d_for_elspline[i+1];
#elif ELFOR_COLL == 3
	  i *= 3;
	  return d_for_elspline[i]+x*(d_for_elspline[i+1]+x*(d_for_elspline[i+2]));
#elif ELFOR_COLL == 4
	  i *= 4;
	  return d_for_elspline[i]+x*(d_for_elspline[i+1]+x*(d_for_elspline[i+2]+x*(d_for_elspline[i+3])));
#endif
}

