/* !!!!! !!!!!!!!!!!!!!!!!!!!!!! !!!!!*/
value_t lj_for_seq(value_t x)
/*
 * direct LJ implementation for input x=r^2
 * for epsilon=1, sigma =1
 */
{	
	return 48.0/(x*x*x*x*x*x*x) - 24.0/(x*x*x*x);
}

value_t lj_pot_seq(value_t x)
/*
 * direct LJ implementation for input x=r^2
 * for epsilon=1, sigma =1
 * variant with
 */
{
	return 4.0/(x*x*x*x*x*x) - 4.0/(x*x*x);
}

