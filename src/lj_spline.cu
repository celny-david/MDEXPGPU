/* Function: lj_for
 * device function which returns the force value for given distance
 *
 * perform lookup in the spline and calculates value of the polynom
 *
 * Notes:
 * > this is the version for the standard interaction
 * > performs out of the bounds check
 * > uses the #defined variables for spline parameters
 * > implemented for 2 - 4 column flattened potentials
 *
 * Parameter:
 * x - the distance value
 */
__device__ value_t lj_for(value_t x)
{

  unsigned int i;

  i=(unsigned int)(x*POT_WIDTH);
  if (i<MIN_SIZE || i> MAX_SIZE)
  {
  	return 0.0;
  }
#if FOR_COLL == 2
	  i *= 2;
	  return d_for_spline[i]+x*d_for_spline[i+1];
#elif FOR_COLL == 3
	  i *= 3;
	  return d_for_spline[i]+x*(d_for_spline[i+1]+x*(d_for_spline[i+2]));
#elif FOR_COLL == 4
	  i *= 4;
	  return d_for_spline[i]+x*(d_for_spline[i+1]+x*(d_for_spline[i+2]+x*(d_for_spline[i+3])));
#endif
}

